int CRunImg2::SetMovieArgs(int tim, CMyString srcInfo, CMyString& dstInfo,
                                    int& numReps)
{
    if (tim < 0) {
        numReps = 3;
    } else {
        numReps = GetMovieStepReverse(tim,6) + 1;
    }
    if (numReps > 1)
        dstInfo.Format("%d calls",numReps);
    return tim;
}
